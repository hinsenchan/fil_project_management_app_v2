    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        // TODO add your handling code here:
        
        String[] array = new String[4];
        array[0] = emailTextField.getText();
        array[1] = usernameTextField.getText();
        array[2] = String.valueOf(enterPasswordTextField.getPassword());
        array[3] = String.valueOf(reenterPasswordTextField.getPassword());
        
        if(array[0].equals("") || array[1].equals("") || array[2].equals("") || array[3].equals("")){
            JOptionPane.showMessageDialog(rootPane, "All the Fields are Mandatory");
        } else {
            if(!array[2].equals(array[3])){
                JOptionPane.showMessageDialog(rootPane, "Passwords Do Not Match");
            } else {
                boolean success = forgotPasswordController.checkCredentials(array[1], array[0], array[2]);
                if(success){
                    JOptionPane.showMessageDialog(rootPane, "Password Successfully Updated");
                    dispose();
                    new Login().setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Username and Emailaddress do not match");
                }
            }
        }
        
        
    }//GEN-LAST:event_submitButtonActionPerformed

