    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:


        final String array[] = new String[8];
        // System.out.println(centerPanel.getComponentCount());

        array[0] = firstNameTextField.getText();
        array[1] = lastNameTextField.getText();
        array[2] = emailTextField.getText();
        array[3] = userNameTextField.getText();
        array[4] = String.valueOf(enterPasswordField.getPassword());

        if (array[0].equals("") || array[1].equals("") || array[2].equals("") || array[3].equals("")
                || array[4].equals("")) {
            JOptionPane.showMessageDialog(centerPanel, "Please enter complete Details with Password Confirmation");
        } else if (!String.valueOf(enterPasswordField.getPassword()).equals(String.valueOf(reenterPasswordField.getPassword()))) {
            JOptionPane.showMessageDialog(null, "Passwords do not match");
            reenterPasswordField.setText("");
            reenterPasswordField.requestFocus();
            return;
        } else if (!adminRadioButton.isSelected() && !viewerRadioButton.isSelected()) {
            JOptionPane.showMessageDialog(null, "Please Select Privileges");

        } else {
            array[5] = phoneNumberTextField.getText();
            array[6] = "1";

            if (adminRadioButton.isSelected()) {
                array[7] = adminRadioButton.getText();
            }
            if (viewerRadioButton.isSelected()) {
                array[7] = viewerRadioButton.getText();
            }
            boolean flag = addnewusercontroller.addUser(array);
            if (flag) {
                JOptionPane.showMessageDialog(centerPanel, "New UserID" + "  " + array[3] + " " + "has been added succesfully");

                //centerPanel.removeAll();
                //centerPanel.repaint();
                //centerPanel.revalidate();
                /*
                AddNewUserPanel addnewusr = new AddNewUserPanel();

                centerPanel.setLayout(new java.awt.BorderLayout());
                centerPanel.removeAll();
                centerPanel.add(addnewusr);
                centerPanel.revalidate();
                centerPanel.repaint();
                */
                
                mainApplication.getContentPanel().remove(mainApplication.getAddNewUserPanel());
                mainApplication.setAddNewUserPanel(new AddNewUserPanel(mainApplication));
                mainApplication.getContentPanel().add(mainApplication.getAddNewUserPanel());
                mainApplication.getContentPanel().revalidate();
                mainApplication.getContentPanel().repaint();                  


            } else {
                JOptionPane optionPane = new JOptionPane("User can not add to databse, verify that user might already exists", JOptionPane.ERROR_MESSAGE);
                JDialog dialog = optionPane.createDialog("Error !");
                dialog.setAlwaysOnTop(true);
                dialog.setVisible(true);
                //initComponents();


            }
        }
    }//GEN-LAST:event_addButtonActionPerformed

