    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {
//GEN-FIRST:event_submitButtonActionPerformed

        // TODO add your handling code here:

        if (username.getText().equals("")) {
            JOptionPane.showMessageDialog(LoginMainPanel, "Please enter your Username to Login");
            username.requestFocus();
            return;
        }

        if (String.valueOf(jPasswordField1.getPassword()).equals("")) {
            JOptionPane.showMessageDialog(LoginMainPanel, "Password field can not be empty, Please enter your Password");
            jPasswordField1.requestFocus();
            return;
        }

        if (!(username.getText().matches("[a-zA-z0-9]*"))) {
            JOptionPane.showMessageDialog(LoginMainPanel, "Username should contain only Alphabets and numbers");
            username.setText("");
            username.requestFocus();
            return;
        }

        String[] array = new String[2];
        array[0] = username.getText();
        array[1] = String.valueOf(jPasswordField1.getPassword());
        logincontroller.submitLogin(array);

        if ((logincontroller.submitLogin(array)).equals("Atrue")) {
            JOptionPane.showMessageDialog(LoginMainPanel, "You will be Logged In as an Administrator");
            
            new Welcome().setVisible(true);
            dispose();
        }
        if ((logincontroller.submitLogin(array)).equals("Vtrue")) {
            JOptionPane.showMessageDialog(LoginMainPanel, "You will be Logged In as a Viewer");
            
            new ViewerWelcome().setVisible(true);
            dispose();
        }
        if ((logincontroller.submitLogin(array)).equals("false")) {
            JOptionPane.showMessageDialog(LoginMainPanel, "Invalid Credentials");
        }
    }//GEN-LAST:event_submitButtonActionPerformed

