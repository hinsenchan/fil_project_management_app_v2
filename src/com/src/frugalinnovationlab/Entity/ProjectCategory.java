package com.src.frugalinnovationlab.Entity;

import java.io.Serializable;
import java.util.Collection;
import java.util.Set;
import javax.persistence.Basic;
import javax.persistence.CascadeType;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.ManyToMany;
import javax.persistence.NamedQueries;
import javax.persistence.NamedQuery;
import javax.persistence.Table;

/**
 *
 * @author Sony
 */
@Entity
@Table(name = "project_category")
@NamedQueries({
    @NamedQuery(name = "ProjectCategory.findAll", query = "SELECT p FROM ProjectCategory p"),
    @NamedQuery(name = "ProjectCategory.findByName", query = "SELECT p FROM ProjectCategory p WHERE p.name = :name")})
public class ProjectCategory implements Serializable {
    private static final long serialVersionUID = 1L;
    @Id
    @Basic(optional = false)
    @Column(name = "name")
    private String name;
    
    @ManyToMany(cascade=CascadeType.ALL, mappedBy = "projectCategories")
    private Set<Project> projects;

    public ProjectCategory() {
    }

    public ProjectCategory(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Set<Project> getProjects() {
        return projects;
    }

    public void setProjects(Set<Project> projects) {
        this.projects = projects;
    }


    @Override
    public int hashCode() {
        int hash = 0;
        hash += (name != null ? name.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof ProjectCategory)) {
            return false;
        }
        ProjectCategory other = (ProjectCategory) object;
        if ((this.name == null && other.name != null) || (this.name != null && !this.name.equals(other.name))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "com.src.frugalinnovationlab.Entity.ProjectCategory[ name=" + name + " ]";
    }
    
}
